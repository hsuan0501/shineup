    }

    const selectedLevel = levelHierarchy[selectedSeries.value] || 0

    // È°ØÁ§∫ÊâÄÊúâÁ¶ÆÂìÅÔºå‰ΩÜÊ®ôË®òÂèØÂÖåÊèõÊÄß
    return mockRewards.map(gift => {
